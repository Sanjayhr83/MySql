(
expression is cobination of operands and operators that evaluates to a single value. It can be used in SELECT, WHERE, GROUP BY, HAVING, and ORDER BY clauses. Examples of expressions include arithmetic expressions, string expressions, date expressions, and logical expressions.
10 + 20 = 30 (10,20,30 is operand and +,= is operator)

1.direct values : the values which we are using directly in a expression are called direct values. Example: 10,20,30
2.column names : the column names or variables we are using indirectly in a expression are called column names. Example: SAL,COMM
