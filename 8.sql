9) WQTD FNAME,SALARY WITH 30% DEDUCTOIN ?
DEDUCTION SYNTAX OR FORMULA : COLUMN_NAME - (COLUMN_NAME * %DEDUCTION / 100)

SELECT FNAME,SAL-(SAL * 30 / 100)
FROM EMPS;

10) WQTD FNAME,COMMISION WITH 15% HIKE ANNUAL 50% DEDUCTION ?
