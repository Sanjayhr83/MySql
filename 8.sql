DISTINCT :IT IS USED TO AVOID VALUES FROM RESULTANT TABLE.

RULES IN DISTINCT :
-------------------
EITHER * OR DISTINCT KEYWORD MUST BE THE FIRST ARGUMENT IN SELECT CLAUSE.
WE CAN USE MULTIPLE CLOUMNS ALONG WITH DISTINCT KEYWORD BUT IT WILL AVOID COMBINATION OF DUPLICATION VALUES.


23) WQTD UNIQUE JOB ROLES FROM EMPS TAMPS ?
