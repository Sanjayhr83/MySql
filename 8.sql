7) WQTD FNAME,STATUS AND LID AND SALARY WITH 10% HIKE ?
HIKE SYNTAX OR FORMULA :  COLUMN_NAME +(COLUMN_NAME * %HIKE / 100)

SELECT FNAME,STATUS,LID,SAL+(SAL * 10 / 100)
FROM EMPS;

		OR

SELECT FNAME,STATUS,LID,SAL*0.1+SAL
FROM EMPS;

8) WQTD FNAME,JOB,CUSTOMER ID AND SALARY WITH 20% HIKE ?
