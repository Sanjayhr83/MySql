To create database :
  Syntax:
    CREATE DATABASE DATABASE_NAME;


To view all the database in Mysql :
  syntax:
    SHOW DATABASES;


To access particular database in mysql :
  syntax:
    USE DATABASE_NAME;

To clear the screen in mysql server :
  syntax:
    SYSTEM CLS;

To close mysql server :
  syntax:
    EXIT

To view all the tables of one database :
  syntax:
    SHOW TABLES;

To view constaints name of all the contraints in the tables :
 step 1:-
        USE INFORMATION_SCHEMA;
 step 2:-
        SELECT * FROM TABLE_CONSTRAINTS;