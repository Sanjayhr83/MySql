66) WQTD AVG SALARY, TOTAL SALARY , MIN SALARY ,MAX SALARY OF THE EMPS IF EMPS FNAME STARING WITH K OR A ?
SELECT AVG(SAL), SUM(SAL), MIN(SAL), MAX(SAL)
FROM EMPS
WHERE FNAME LIKE "K%" OR FNAME LIKE "A%";

67) WQTD NUMBER OF EMPS WORKING AS WAITER OR DELIVERY ?
SELECT COUNT(*)
FROM EMPS
WHERE JOB="WAITER" OR JOB="DELIVERY";

68) WQTD NUMBER OF UNIQUE JOB ROLE FROM EMPS TABLE ?
SELECT COUNT(DISTINCT JOB)
FROM EMPS;

69) WQTD DIFFERENCE BETWEEN NUMBER OF JOB ROLE AND NUMBER OF UNIQUE JOB ROLES ?
SELECT COUNT(JOB)-COUNT(DISTINCT JOB)
FROM EMPS;
